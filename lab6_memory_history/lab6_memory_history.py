    # –í—Ç–æ—Ä–æ–µ –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏–µ - –ø—Ä–æ–≤–µ—Ä—è–µ–º, –ø–æ–º–Ω–∏—Ç –ª–∏ —Å–∏—Å—Ç–µ–º–∞ –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    user_input2 = "–†–∞—Å—Å–∫–∞–∂–∏ –º–Ω–µ –æ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö –¥–æ—Å—Ç–∏–∂–µ–Ω–∏—è—Ö –≤ –æ–±–ª–∞—Å—Ç–∏ –∫–æ–º–ø—å—é—Ç–µ—Ä–Ω–æ–≥–æ –∑—Ä–µ–Ω–∏—è."
    
    # –í—ã–∑—ã–≤–∞–µ–º –º–æ–¥–µ–ª—å —Å–Ω–æ–≤–∞, –ø–µ—Ä–µ–¥–∞–≤–∞—è –æ–±–Ω–æ–≤–ª–µ–Ω–Ω—É—é –∏—Å—Ç–æ—Ä–∏—é
    response2 = chain.invoke({
        "history": conversation_history,
        "input": user_input2
    })
    
    # –í—ã–≤–æ–¥–∏–º –æ—Ç–≤–µ—Ç
    print(f"\n–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: {user_input2}")
    print(f"–ê—Å—Å–∏—Å—Ç–µ–Ω—Ç: {response2}")
    
    # –î–æ–±–∞–≤–ª—è–µ–º –Ω–æ–≤—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ –∏—Å—Ç–æ—Ä–∏—é
    conversation_history.append(HumanMessage(content=user_input2))
    conversation_history.append(AIMessage(content=response2))
    
    # –¢—Ä–µ—Ç—å–µ –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏–µ - –ø—Ä–æ–≤–µ—Ä—è–µ–º, –ø–æ–º–Ω–∏—Ç –ª–∏ —Å–∏—Å—Ç–µ–º–∞ –ø—Ä–µ–¥—ã–¥—É—â–∏–π –≤–æ–ø—Ä–æ—Å
    user_input3 = "–ê –∫–∞–∫–∏–µ –µ—â–µ –æ–±–ª–∞—Å—Ç–∏ –ò–ò —Å–µ–π—á–∞—Å –∞–∫—Ç–∏–≤–Ω–æ —Ä–∞–∑–≤–∏–≤–∞—é—Ç—Å—è –ø–æ–º–∏–º–æ –∫–æ–º–ø—å—é—Ç–µ—Ä–Ω–æ–≥–æ –∑—Ä–µ–Ω–∏—è?"
    
