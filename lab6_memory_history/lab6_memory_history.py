def create_chatbot_with_memory(memory_type="buffer", memory_params=None):
    """
    –°–æ–∑–¥–∞–µ—Ç —á–∞—Ç-–±–æ—Ç–∞ —Å —É–∫–∞–∑–∞–Ω–Ω—ã–º —Ç–∏–ø–æ–º –ø–∞–º—è—Ç–∏.
    
    Args:
        memory_type (str): –¢–∏–ø –ø–∞–º—è—Ç–∏ ("buffer", "window", "token", "summary")
        memory_params (dict): –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –¥–ª—è –ø–∞–º—è—Ç–∏
    
    Returns:
        function: –§—É–Ω–∫—Ü–∏—è —á–∞—Ç-–±–æ—Ç–∞, –ø—Ä–∏–Ω–∏–º–∞—é—â–∞—è —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    """
    # –ü–∞—Ä–∞–º–µ—Ç—Ä—ã –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
    if memory_params is None:
        memory_params = {}
    
    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –º–æ–¥–µ–ª—å
    model = ChatOpenAI(model="gpt-4o-mini", temperature=0.7)
    
    # –í—ã–±–∏—Ä–∞–µ–º —Ç–∏–ø –ø–∞–º—è—Ç–∏
    if memory_type == "buffer":
        memory = ConversationBufferMemory(return_messages=True, memory_key="history")
    elif memory_type == "window":
        k = memory_params.get("k", 5)
        memory = ConversationBufferWindowMemory(k=k, return_messages=True, memory_key="history")
    elif memory_type == "token":
        max_token_limit = memory_params.get("max_token_limit", 500)
        memory = ConversationTokenBufferMemory(
            llm=model, max_token_limit=max_token_limit, return_messages=True, memory_key="history"
        )
    elif memory_type == "summary":
        memory = ConversationSummaryMemory(llm=model, return_messages=True, memory_key="history")
    else:
        raise ValueError(f"–ù–µ–ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º—ã–π —Ç–∏–ø –ø–∞–º—è—Ç–∏: {memory_type}")
    
    # –°–æ–∑–¥–∞–µ–º –ø—Ä–æ–º–ø—Ç —Å –ø–ª–µ–π—Å—Ö–æ–ª–¥–µ—Ä–æ–º –¥–ª—è –∏—Å—Ç–æ—Ä–∏–∏
    prompt = ChatPromptTemplate.from_messages([
        ("system", """–í—ã - –¥—Ä—É–∂–µ–ª—é–±–Ω—ã–π –∏ –ø–æ–ª–µ–∑–Ω—ã–π –∞—Å—Å–∏—Å—Ç–µ–Ω—Ç –ø–æ –∏–º–µ–Ω–∏ –ê–ª–∏—Å–∞. 
        
        –£ –≤–∞—Å —Å–ª–µ–¥—É—é—â–∏–µ —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∏:
        1. –í—ã –æ—Ç–≤–µ—á–∞–µ—Ç–µ –∫—Ä–∞—Ç–∫–æ –∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ç–∏–≤–Ω–æ
        2. –í—ã —Å—Ç–∞—Ä–∞–µ—Ç–µ—Å—å –ø–æ–º–æ—á—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é —Ä–µ—à–∏—Ç—å –µ–≥–æ –∑–∞–¥–∞—á—É
        3. –í—ã –∏—Å–ø–æ–ª—å–∑—É–µ—Ç–µ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –∏–∑ –ø—Ä–µ–¥—ã–¥—É—â–∏—Ö —Å–æ–æ–±—â–µ–Ω–∏–π –¥–ª—è –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞
        4. –í—ã –º–æ–∂–µ—Ç–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞—Ç—å —Ä–∞–∑–≥–æ–≤–æ—Ä –Ω–∞ —Ä–∞–∑–ª–∏—á–Ω—ã–µ —Ç–µ–º—ã
        
        –ò–º–µ–π—Ç–µ –≤ –≤–∏–¥—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–µ –ø—Ä–µ–¥–ø–æ—á—Ç–µ–Ω–∏—è –∏ –æ—Ç–≤–µ—á–∞–π—Ç–µ –≤ –¥—Ä—É–∂–µ–ª—é–±–Ω–æ–º —Ç–æ–Ω–µ.
        """),
        MessagesPlaceholder(variable_name="history"),
        ("human", "{input}")
    ])
    
    # –°–æ–∑–¥–∞–µ–º —Ü–µ–ø–æ—á–∫—É
    chain = prompt | model | StrOutputParser()
    
    # –§—É–Ω–∫—Ü–∏—è —á–∞—Ç-–±–æ—Ç–∞
    def chatbot(user_input):
        # –ó–∞–≥—Ä—É–∂–∞–µ–º —Ç–µ–∫—É—â—É—é –∏—Å—Ç–æ—Ä–∏—é –∏–∑ –ø–∞–º—è—Ç–∏
        memory_vars = memory.load_memory_variables({})
        
        # –í—ã–∑—ã–≤–∞–µ–º –º–æ–¥–µ–ª—å
        response = chain.invoke({
            "history": memory_vars.get("history", []),
            "input": user_input
        })
        
        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏–µ –≤ –ø–∞–º—è—Ç–∏
        memory.save_context({"input": user_input}, {"output": response})
        
        return response
    
    return chatbot, memory

