def demonstrate_memory_types():
    """
    –î–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏—è —Ä–∞–∑–ª–∏—á–Ω—ã—Ö —Ç–∏–ø–æ–≤ –ø–∞–º—è—Ç–∏ –≤ LangChain –∏ –∏—Ö –æ—Å–æ–±–µ–Ω–Ω–æ—Å—Ç–µ–π.
    """
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –æ–±—â–∏–π –ø—Ä–æ–º–ø—Ç –¥–ª—è –≤—Å–µ—Ö —ç–∫—Å–ø–µ—Ä–∏–º–µ–Ω—Ç–æ–≤
    prompt = ChatPromptTemplate.from_messages([
        ("system", "–í—ã - –¥—Ä—É–∂–µ–ª—é–±–Ω—ã–π –ø–æ–º–æ—â–Ω–∏–∫. –û—Ç–≤–µ—á–∞–π—Ç–µ –∫—Ä–∞—Ç–∫–æ –∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ç–∏–≤–Ω–æ."),
        MessagesPlaceholder(variable_name="history"),
        ("human", "{input}")
    ])
    
    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –º–æ–¥–µ–ª—å
    model = ChatOpenAI(model="gpt-4o-mini", temperature=0)
    
    # –°–æ–∑–¥–∞–µ–º —Ü–µ–ø–æ—á–∫—É: –ø—Ä–æ–º–ø—Ç -> –º–æ–¥–µ–ª—å -> –ø–∞—Ä—Å–µ—Ä
