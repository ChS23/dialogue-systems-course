    message_history.add_user_message("–†–∞—Å—Å–∫–∞–∂–∏ –º–Ω–µ –æ —Ç–∏–ø–∞—Ö –Ω–µ–π—Ä–æ–Ω–Ω—ã—Ö —Å–µ—Ç–µ–π –∏ –∏—Ö –ø—Ä–∏–º–µ–Ω–µ–Ω–∏–∏.")
    
    # –í—ã–≤–æ–¥–∏–º –≤—Å–µ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ –∏—Å—Ç–æ—Ä–∏–∏
    print("\n–ò—Å—Ç–æ—Ä–∏—è —Å–æ–æ–±—â–µ–Ω–∏–π —á–µ—Ä–µ–∑ ChatMessageHistory:")
    for message in message_history.messages:
        if isinstance(message, HumanMessage):
            print(f"–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: {message.content}")
        elif isinstance(message, AIMessage):
            print(f"–ê—Å—Å–∏—Å—Ç–µ–Ω—Ç: {message.content}")
    
    # –°–æ–∑–¥–∞–µ–º –º–æ–¥–µ–ª—å
